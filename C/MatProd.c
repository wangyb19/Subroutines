void matprod(double a[6][6], double b[6][6], double c[6][6])
{
    int i,j,k;
    for (j=0;j<6;j++)
    {
        for (k=0;k<6;k++)
        {
            c[j][k]=0.0;
            for (i=0;i<6;i++)
            {
                c[j][k]+=a[j][i]*b[i][k];
            }
        }
    }    
}